    private void cargarCliente() {
        Log.set(Log.LEVEL_DEBUG);
        client = new Client();
        client.start();
        Network.register(client);
        client.addListener(new Listener() {
            @Override
            public void connected(Connection connection) {
                ValidacionConnection vc = new ValidacionConnection();
                vc.user = Network.LOCALUSER;
                client.sendTCP(vc);
            }

            @Override
            public void disconnected(Connection connection) {
                System.out.println("El servidor se ha caido:");
            }

            @Override
            public void received(Connection connection, Object object) {
                if(object instanceof Network.InvalidConnection){
                    JOptionPane.showMessageDialog(rootPane,
                            "Coneccion rechasada.. usuario invalido");
                    return;
                }
                if (object instanceof String) {
                    String asd = (String) object;
                    if (asd.charAt(0) == '7') {//el 7 es de los print de arduino
                        final String rec = asd.substring(1);

                        /*SwingUtilities.invokeLater(new Runnable() {
                        public void run() {
                        /*HACER ALGO
                        }
                        });*/
                        try {
                            String t_v[] = rec.split("\\|");//nombre y valor
                            t_v[1] = t_v[1].replace(";", "");
                            System.out.println(t_v[1]);
                            if (t_v[0].equalsIgnoreCase("RL01")) {

                                if (Integer.parseInt(t_v[1]) == 0) {//significa que esta pagado
                                    lbl_estado.setText("Apagado");
                                } else {
                                    lbl_estado.setText("Encendido");
                                }

                            }
                        } catch (Exception pene) {
                            System.out.println(pene.getMessage());
                        }
                    }

                }
            }
        });
    }
